/**
 * НАСТРОЙКИ ПОВЕДЕНИЯ AR-СКАНЕРА
 * -----------------------------------------------------------------------
 * Единственное место, где стоит менять тайминги и шаги анимации —
 * значения подхватываются в app.js и panel.js через window.APP_CONFIG.
 * За внешний вид карточки (ширина, положение, размер фото) отвечает
 * ДРУГОЙ файл — css/config.css.
 * -----------------------------------------------------------------------
 */
window.APP_CONFIG = {
  // Сколько мс держать экран без единой распознанной метки, прежде чем
  // показать подсказку "метки не найдены" / кнопку ручного ввода.
  NO_DETECTION_TIMEOUT_MS: 3000,

  // Не запрашивать повторно данные по той же метке чаще, чем раз в это
  // время (мс) — метка может попадать в кадр по многу раз в секунду.
  RESCAN_COOLDOWN_MS: 1500,

  // Через сколько мс без метки в кадре убирать её карточку с экрана.
  // Больше значение — карточка дольше "держится" на месте, даже если
  // метка на секунду пропала из кадра (заслонили рукой, дёрнули камеру).
  PANEL_MAX_AGE_MS: 2500,

  // Как часто (мс) проверять и убирать "протухшие" карточки.
  PRUNE_INTERVAL_MS: 400,

  // Сглаживание позиции карточки (0..1). За каждый кадр карточка
  // проезжает только эту ДОЛЮ пути до новой сырой координаты метки —
  // остальное "доедет" на следующих кадрах. Так один сбойный кадр
  // детектора (блик, перекрытие) не швыряет карточку в сторону.
  // Меньше число — карточка стабильнее, но чуть "тяжелее" реагирует на
  // реальное движение метки. Больше (ближе к 1) — резче и быстрее, но
  // более дёрганая при плохом распознавании.
  POSITION_SMOOTHING: 0.35,

  // ---- Разъезд карточек, если несколько меток рядом (panel.js: resolveCollisions) ----
  // На сколько пикселей карточка сдвигается за один шаг.
  COLLISION_STEP_PX: 14,
  // Не пересчитывать сдвиг чаще, чем раз в это время (мс) — если сделать
  // слишком часто, карточки будут дёргаться быстрее, чем успевает
  // доиграть плавный CSS-переход.
  COLLISION_RESOLVE_INTERVAL_MS: 220,
  // Минимальный зазор (px) между карточками, при котором они ещё
  // считаются "не наехавшими друг на друга" — чем БОЛЬШЕ число, тем
  // раньше (при большем расстоянии) карточки начнут раздвигаться.
  COLLISION_PADDING_PX: 6,
};
